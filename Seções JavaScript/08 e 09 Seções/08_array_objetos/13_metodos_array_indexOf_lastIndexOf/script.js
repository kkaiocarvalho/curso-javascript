let numeros = [5,6,2,4,9,6,2];

// - indexOf: encontra o índice de um determinado elemento;
console.log(numeros.indexOf(2)); // aqui busco o índice 2
// - lastIndexOf: encontra o último índice de um elemento;
console.log(numeros.lastIndexOf(2)); // aqui busco o último índice do elemento 2 no caso vai ser o índice de valor 6 que será retornado


console.log(numeros.lastIndexOf(9)); // retorna 4
