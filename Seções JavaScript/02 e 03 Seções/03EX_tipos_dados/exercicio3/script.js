// ESCREVA TRÊS COMPARAÇÕES EM BOOLEAN;
// UMA COM MAIOR, MENOR OU IGUAL E DIFERENTE;


//maior
console.log(10 > 5); //true
console.log(15 > 20); //false
//menor
console.log(77 < 177); //true
console.log(200 < 100); //false
//maior ou igual
console.log(10 >= 5); //true
console.log(10 >= 10); //true
console.log(10 >= 15); //false
//menor ou igual
console.log(40 <= 25); //false
console.log(40 <= 40); //true
console.log(40 <= 45); //true
// diferente
console.log(2 != 3); //true
console.log(6 != 6); //false
console.log(10 != 1); //true
// misturando operadores
console.log(10 > 5 && 10 <= 10 && 10 != 5); //true
//com string
console.log("Kaio" != "Sergio"); //true
console.log("Sergio" != 'Sergio'); //false
